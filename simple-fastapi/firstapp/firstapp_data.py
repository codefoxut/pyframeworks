from enum import Enum
from typing import Optional, List, Set

from pydantic import BaseModel, Field, HttpUrl, EmailStr


class ModelName(str, Enum):
    alexnet = "alexnet"
    resnet = "resnet"
    lenet = "lenet"


class NeedyName(str, Enum):
    veryneedy = "veryneedy"
    normal = "normal"
    notneedy = "notneedy"


